#!/usr/bin/env python3
"""Experiment lifecycle — draft, approve, run, score, retire.

HARD RAILS (locked):
- Max 2 concurrent experiments. approve() refuses a third.
- Spawned agents are CONFIGS instantiated from experiment-template/ —
  never novel code. run() below is the only code that executes them, and
  all it does is construct a writer --assignment call from the config.
- Spawning ALWAYS requires the operator's approval: the analyst drafts a
  manifest into approval-queue/; nothing is instantiated until
  `python3 orchestrator/run.py approve <manifest-file>` is run.
- Experiment content goes through the same grader + approval flow as
  everything else (it is generated by the same maker agent).

Lifecycle: analyst detects signal -> draft_manifest() -> approval-queue/
-> operator approves -> approve() instantiates experiments/<id>/ + schedules
-> run() generates posts per quota -> day 14: analyst calls score()
-> verdict -> retire() moves losers to experiments/graveyard/<id>/ with a
one-paragraph post-mortem (winners get a keep-or-fold note for the
operator — promotion to a permanent agent is a human call, never automatic).
"""
import json
import os
import re
import subprocess
import sys
from datetime import date, datetime, timedelta

ORCH = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(ORCH)
QUEUE = os.path.join(ROOT, "approval-queue")
EXP_DIR = os.path.join(ROOT, "experiments")
GRAVEYARD = os.path.join(EXP_DIR, "graveyard")
TEMPLATE = os.path.join(ROOT, "experiment-template", "config.template.json")
STATE_FILE = os.path.join(ORCH, "state.json")
SCHEDULE_FILE = os.path.join(ORCH, "schedule.json")
MAX_CONCURRENT = 2
MOCK = os.environ.get("ORCH_MOCK") == "1"


def _load(p, d):
    try:
        with open(p) as f:
            return json.load(f)
    except (OSError, ValueError):
        return d


def _save(p, o):
    tmp = p + ".tmp"
    with open(tmp, "w") as f:
        json.dump(o, f, indent=2)
    os.replace(tmp, p)


def _slug(s):
    return re.sub(r"[^a-z0-9]+", "-", s.lower()).strip("-")[:40]


# ---------------------------------------------------------------------------
def draft_manifest(signal):
    """Called by the analyst when a threshold signal fires. Writes a manifest
    to approval-queue/ and returns its path. Never instantiates anything.
    Under ORCH_MOCK=1 the draft goes to orchestrator/mockrun/ instead — a
    mock-derived manifest must never sit in the real approval queue where a
    weekly review could mistake it for a live signal."""
    queue = os.path.join(ORCH, "mockrun") if MOCK else QUEUE
    os.makedirs(queue, exist_ok=True)
    exp_id = "exp-%s-%s" % (date.today().strftime("%Y%m%d"), _slug(signal["name"]))
    manifest = {
        "type": "experiment-manifest",
        "id": exp_id,
        "created": datetime.now().isoformat(timespec="seconds"),
        "status": "awaiting-approval",
        "hypothesis": signal["hypothesis"],
        "signal": signal,
        "duration_days": 14,
        "post_quota": signal.get("post_quota", 4),
        "success_metric": signal.get("success_metric",
                                     "UTM-attributed conversions for campaign %s >= 3 in 14 days" % exp_id),
        "kill_criteria": signal.get("kill_criteria",
                                    "0 conversions AND engagement below account median by day 10, or any brand-rule breach"),
        "config": {
            "topic_focus": signal.get("topic_focus", ""),
            "platform": signal.get("platform", "instagram"),
            "hook_style": signal.get("hook_style", ""),
            "utm_campaign": exp_id,
            "posts_per_run": 1,
        },
    }
    path = os.path.join(queue, exp_id + ".json")
    _save(path, manifest)
    md = os.path.join(queue, exp_id + ".md")
    with open(md, "w") as f:
        f.write("# Experiment proposal — %s\n\n**Hypothesis:** %s\n\n**Why now (signal):** %s\n\n"
                "**Duration:** 14 days · **Quota:** %d posts · **Success:** %s\n\n**Kill criteria:** %s\n\n"
                "To approve: `python3 orchestrator/run.py approve %s`\n"
                % (manifest["id"], manifest["hypothesis"], signal.get("evidence", "-"),
                   manifest["post_quota"], manifest["success_metric"], manifest["kill_criteria"],
                   os.path.basename(path)))
    print("manifest drafted -> %s" % path)
    return path


def approve(manifest_name):
    """Instantiate an approved experiment. Refuses if rails are violated."""
    path = manifest_name if os.path.isabs(manifest_name) else os.path.join(QUEUE, os.path.basename(manifest_name))
    m = _load(path, None)
    if not m or m.get("type") != "experiment-manifest":
        raise SystemExit("not an experiment manifest: %s" % path)
    state = _load(STATE_FILE, {})
    active = state.setdefault("experiments", {}).setdefault("active", [])
    if len(active) >= MAX_CONCURRENT:
        raise SystemExit("RAIL: already %d active experiments (max %d) — retire one first." % (len(active), MAX_CONCURRENT))
    if any(e["id"] == m["id"] for e in active):
        raise SystemExit("already active: %s" % m["id"])
    template = _load(TEMPLATE, {})
    cfg = dict(template)
    cfg.update(m["config"])
    cfg["id"] = m["id"]
    cfg["hypothesis"] = m["hypothesis"]
    exp_home = os.path.join(EXP_DIR, m["id"])
    os.makedirs(exp_home, exist_ok=True)
    _save(os.path.join(exp_home, "config.json"), cfg)
    _save(os.path.join(exp_home, "manifest.json"), {**m, "status": "active"})
    # register in schedule.json (Tue+Fri 7:00 so experiment posts don't collide with the Mon+Thu batch)
    sched = _load(SCHEDULE_FILE, [])
    sched.append({"agent": m["id"], "when": [{"weekday": 2, "hour": 7, "minute": 0}, {"weekday": 5, "hour": 7, "minute": 0}],
                  "note": "experiment — auto-added by approve(); removed at retirement"})
    _save(SCHEDULE_FILE, sched)
    started = date.today()
    active.append({"id": m["id"], "started": started.isoformat(),
                   "ends": (started + timedelta(days=m["duration_days"])).isoformat(),
                   "posts_made": 0, "post_quota": m["post_quota"]})
    _save(STATE_FILE, state)
    m["status"] = "approved"
    _save(path, m)
    print("APPROVED %s -> %s (registered Tue+Fri 7:00; content still flows through the grader + your approval)" % (m["id"], exp_home))


def run(exp_id):
    """Execute one scheduled run of an experiment agent: construct a maker
    assignment from its CONFIG (never code) and hand it to the standard writer."""
    cfg = _load(os.path.join(EXP_DIR, exp_id, "config.json"), None)
    if not cfg:
        raise SystemExit("no config for %s" % exp_id)
    state = _load(STATE_FILE, {})
    entry = next((e for e in state.get("experiments", {}).get("active", []) if e["id"] == exp_id), None)
    if not entry:
        print("%s is not active (retired or unapproved) — skipping." % exp_id)
        return 0
    if entry["posts_made"] >= entry["post_quota"]:
        print("%s quota reached (%d/%d) — no more posts; awaiting day-14 scoring." % (exp_id, entry["posts_made"], entry["post_quota"]))
        return 0
    assignment = ("EXPERIMENT %s (campaign tag %s): %s. Platform focus: %s. %s"
             "Write ONE package. Tag every outbound link with utm_campaign=%s."
             % (exp_id, cfg["utm_campaign"], cfg.get("topic_focus") or cfg.get("hypothesis", ""),
                cfg.get("platform", "instagram"),
                ("Hook style: %s. " % cfg["hook_style"]) if cfg.get("hook_style") else "",
                cfg["utm_campaign"]))
    if MOCK:
        print("[MOCK] would run: node agents/agent3-writer.js --assignment %r" % assignment)
        rc = 0
    else:
        rc = subprocess.call(["node", "agents/agent3-writer.js", "--assignment", assignment], cwd=ROOT, timeout=45 * 60)
    if rc == 0:
        entry["posts_made"] += 1
        _save(STATE_FILE, state)
    return rc


def score(exp_id, analytics):
    """Day-14 verdict against the experiment's OWN manifest. analytics is the
    analyst's parsed analytics dict (see analyst.py / analytics-inbox.json)."""
    manifest = _load(os.path.join(EXP_DIR, exp_id, "manifest.json"), {})
    conversions = 0
    for row in analytics.get("utm_campaigns", []):
        if row.get("campaign") == exp_id:
            conversions = row.get("conversions", 0)
    target = 3
    mtxt = manifest.get("success_metric", "")
    m = re.search(r">=\s*(\d+)", mtxt)
    if m:
        target = int(m.group(1))
    verdict = "win" if conversions >= target else "loss"
    return {"id": exp_id, "conversions": conversions, "target": target, "verdict": verdict,
            "success_metric": mtxt, "hypothesis": manifest.get("hypothesis", "")}


def retire(exp_id, result):
    """Move a finished experiment to the graveyard (losers) or mark it for
    the operator's keep-or-fold decision (winners). Deschedules either way."""
    state = _load(STATE_FILE, {})
    ex = state.setdefault("experiments", {})
    ex["active"] = [e for e in ex.get("active", []) if e["id"] != exp_id]
    ex.setdefault("history", []).append({**result, "retired": date.today().isoformat()})
    _save(STATE_FILE, state)
    sched = _load(SCHEDULE_FILE, [])
    _save(SCHEDULE_FILE, [j for j in sched if j.get("agent") != exp_id])
    exp_home = os.path.join(EXP_DIR, exp_id)
    if result["verdict"] == "loss":
        os.makedirs(GRAVEYARD, exist_ok=True)
        dest = os.path.join(GRAVEYARD, exp_id)
        if os.path.isdir(exp_home) and not os.path.isdir(dest):
            os.rename(exp_home, dest)
        with open(os.path.join(dest if os.path.isdir(dest) else GRAVEYARD, "post-mortem.md"), "w") as f:
            f.write("# Post-mortem — %s\n\nHypothesis: %s\n\nResult: %d UTM-attributed conversions vs a target of %d "
                    "over 14 days — did not clear its own success metric (%s). Retired %s. "
                    "One paragraph, one lesson: the audience signal that spawned this wasn't strong enough to "
                    "survive contact with real distribution; the next manifest in this territory needs either a "
                    "sharper hook angle or a different platform before it deserves another 14 days.\n"
                    % (exp_id, result.get("hypothesis", "-"), result["conversions"], result["target"],
                       result.get("success_metric", "-"), date.today().isoformat()))
        print("RETIRED %s -> graveyard (loss: %d/%d conversions)" % (exp_id, result["conversions"], result["target"]))
    else:
        with open(os.path.join(exp_home, "WINNER-DECIDE.md"), "w") as f:
            f.write("# %s WON (%d conversions vs target %d)\n\nPromotion to a permanent agent is YOUR call — "
                    "the orchestrator never auto-promotes. To keep it running, re-approve a fresh manifest "
                    "or fold its config into the main maker rotation.\n" % (exp_id, result["conversions"], result["target"]))
        print("WINNER %s (%d/%d conversions) — descheduled, decision file written: %s/WINNER-DECIDE.md" % (exp_id, result["conversions"], result["target"], exp_home))


if __name__ == "__main__":
    if len(sys.argv) >= 3 and sys.argv[1] == "run":
        sys.exit(run(sys.argv[2]))
    if len(sys.argv) >= 3 and sys.argv[1] == "approve":
        approve(sys.argv[2])
    else:
        raise SystemExit(__doc__)
